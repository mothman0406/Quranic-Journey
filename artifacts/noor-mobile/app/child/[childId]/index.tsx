function GoalPreview({ goal }: { goal: Goal }) {
  const current = goal.currentCount ?? 0;
  const target = goal.targetSurahCount ?? 0;
  const percent = target > 0 ? formatPercent(current, target) : 0;
  const dateLabel = formatGoalDate(goal.targetDate);

  return (
    <View style={styles.goalRow}>
      <View style={styles.goalText}>
        <Text style={styles.goalTitle}>{goal.title}</Text>
        <Text style={styles.goalDetail} numberOfLines={2}>
          {goal.description}
        </Text>
        {target > 0 ? (
          <Text style={styles.goalMeta}>
            {current}/{target} surahs{dateLabel ? ` · by ${dateLabel}` : ""}
          </Text>
        ) : null}
      </View>
      {target > 0 ? (
        <View style={styles.goalProgress}>
          <Text style={styles.goalPercent}>{percent}%</Text>
          <ProgressBar value={percent} color="#2563eb" />
        </View>
      ) : null}
    </View>
  );
}

function AchievementPreview({ achievement }: { achievement: Achievement }) {
  const progress = achievement.progress ?? 0;
  const target = achievement.target ?? 0;
  const percent = achievement.earned ? 100 : target > 0 ? formatPercent(progress, target) : 0;

  return (
    <View style={styles.achievementRow}>
      <View style={[styles.achievementIcon, achievement.earned && styles.achievementIconEarned]}>
        <Text style={styles.achievementEmoji}>{achievement.icon}</Text>
      </View>
      <View style={styles.achievementText}>
        <View style={styles.achievementTitleRow}>
          <Text style={styles.achievementTitle}>{achievement.title}</Text>
          {achievement.earned ? (
            <Text style={styles.earnedText}>Earned</Text>
          ) : target > 0 ? (
            <Text style={styles.achievementProgressText}>
              {progress}/{target}
            </Text>
          ) : null}
        </View>
        <Text style={styles.achievementDetail}>{achievement.description}</Text>
        {!achievement.earned && target > 0 ? (
          <ProgressBar value={percent} color="#f59e0b" />
        ) : null}
      </View>
    </View>
  );
}

