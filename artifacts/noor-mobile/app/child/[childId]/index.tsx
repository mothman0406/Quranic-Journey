export default function ChildDashboard() {
  const { childId, name } = useLocalSearchParams<{ childId: string; name: string }>();
  const router = useRouter();
  const [state, setState] = useState<DashboardState>({ status: "loading" });
  const [refreshing, setRefreshing] = useState(false);
  const navChildId = typeof childId === "string" ? childId : undefined;
  const navName = typeof name === "string" ? name : "";
  const navReviewCount =
    state.status === "ok"
      ? state.reviews?.dueToday.length ?? state.dashboard.reviewsDueToday
      : undefined;

  const loadDashboard = useCallback(
    async (mode: "initial" | "refresh" = "initial") => {
      const childIdParam = childId;
      const nameParam = name;

      if (mode === "initial") {
        setState({ status: "loading" });
      } else {
        setRefreshing(true);
      }

      if (!isValidChildId(childIdParam)) {
        setState({
          status: "error",
          message: "This dashboard route is missing a valid child id. Go back and reopen the child profile.",
        });
        setRefreshing(false);
        return;
      }

      try {
        let dashboard: DashboardResponse;
        try {
          dashboard = await apiFetch<DashboardResponse>(
            `/api/children/${childIdParam}/dashboard`,
          );
        } catch {
          await wait(350);
          dashboard = await apiFetch<DashboardResponse>(
            `/api/children/${childIdParam}/dashboard`,
          );
        }
        let reviews: ReviewQueueResponse | null = null;
        let reviewError: string | null = null;

        try {
          reviews = await fetchReviewQueue(childIdParam);
        } catch (e) {
          reviewError = describeError(e);
        }

        setState({
          status: "ok",
          dashboard,
          reviews,
          reviewError,
          dashboardError: null,
        });
      } catch (e) {
        const dashboardError = describeError(e);
        let reviews: ReviewQueueResponse | null = null;
        let reviewError: string | null = null;
        let child: ChildDetail | null = null;

        try {
          reviews = await fetchReviewQueue(childIdParam);
        } catch (reviewFetchError) {
          reviewError = describeError(reviewFetchError);
        }

        try {
          child = await apiFetch<ChildDetail>(`/api/children/${childIdParam}`);
        } catch {
          child = fallbackChild(childIdParam, nameParam);
        }

        setState({
          status: "ok",
          dashboard: fallbackDashboard(child, reviews),
          reviews,
          reviewError,
          dashboardError,
        });
      } finally {
        setRefreshing(false);
      }
    },
    [childId, name],
  );

  useFocusEffect(
    useCallback(() => {
      loadDashboard();
    }, [loadDashboard]),
  );

  function handleCardPress(key: string) {
    if (key === "review") {
      router.push({
        pathname: "/child/[childId]/review",
        params: { childId, name: name ?? "" },
      });
    } else if (key === "reading") {
      router.push({
        pathname: "/child/[childId]/mushaf",
        params: { childId, name: name ?? "" },
      });
    } else if (key === "memorization") {
      router.push({
        pathname: "/child/[childId]/memorization",
        params: { childId, name: name ?? "" },
      });
    }
  }

  function handleTargetsPress() {
    if (!isValidChildId(childId)) return;

    router.push({
      pathname: "/child/[childId]/targets",
      params: { childId, name: name ?? "" },
    });
  }

  function openChildRoute(
    pathname:
      | "/child/[childId]/memorization"
      | "/child/[childId]/review"
      | "/child/[childId]/mushaf"
      | "/child/[childId]/targets"
      | "/child/[childId]/profile"
      | "/child/[childId]/more",
    displayName = name ?? "",
  ) {
    if (!isValidChildId(childId)) return;
    router.push({
      pathname,
      params: { childId, name: displayName },
    });
  }

  function renderContent() {
    if (state.status === "loading") {
      return (
        <View style={styles.center}>
          <ActivityIndicator size="large" color="#2563eb" />
        </View>
      );
    }

    if (state.status === "error") {
      return (
        <View style={styles.center}>
          <Text style={styles.errorText}>{state.message}</Text>
          <Pressable style={styles.retryButton} onPress={() => loadDashboard()}>
            <Text style={styles.retryText}>Retry</Text>
          </Pressable>
        </View>
      );
    }

    const { dashboard, reviews, reviewError, dashboardError } = state;
    const child = dashboard.child;
    const todaysMem = dashboard.todaysPlan.newMemorization;
    const stats = dashboard.memorizationStats;
    const goals = dashboard.goals ?? [];
    const achievements = dashboard.achievements ?? [];
    const earnedAchievements = achievements.filter((achievement) => achievement.earned);
    const todayProgress = dashboard.todayProgress;
    const memStatus = todayProgress?.memStatus ?? "not_started";
    const memSurah = todaysMem?.currentWorkSurahName ?? todaysMem?.surahName;
    const memAyahStart = todaysMem?.currentWorkAyahStart ?? todaysMem?.ayahStart;
    const memAyahEnd = todaysMem?.currentWorkAyahEnd ?? todaysMem?.ayahEnd;
    const memPageLabel = formatPageRange(todaysMem?.pageStart, todaysMem?.pageEnd);
    const memDetail = !todaysMem
      ? stats?.totalSurahsMemorized
        ? "No new memorization assigned today. Keep the memory fresh with review or reading."
        : "No memorization assigned yet. Start with today's first lesson when you're ready."
      : memStatus === "completed"
      ? `${todaysMem.workLabel ?? "Memorization"} complete for ${todaysMem.surahName}.`
      : memStatus === "in_progress"
      ? `${memSurah} is in progress · ${formatAyahRange(memAyahStart, memAyahEnd)}${
          memPageLabel ? ` · ${memPageLabel}` : ""
        }`
      : `${todaysMem.workLabel ?? "New Memorization"} · ${memSurah} · ${formatAyahRange(
          memAyahStart,
          memAyahEnd,
        )}${memPageLabel ? ` · ${memPageLabel}` : ""}`;
    const memCta =
      memStatus === "completed" ? "Done" : memStatus === "in_progress" ? "Continue" : "Start";

    const dueItems = reviews?.dueToday ?? [];
    const reviewedItems = reviews?.reviewedToday ?? [];
    const reviewsDueToday = reviews ? dueItems.length : dashboard.reviewsDueToday;
    const reviewCompletedToday = Math.max(
      todayProgress?.reviewCompletedCount ?? 0,
      reviewedItems.length,
    );
    const reviewTarget =
      todayProgress?.reviewTargetCount != null
        ? Math.max(todayProgress.reviewTargetCount, reviewCompletedToday)
        : reviewsDueToday + reviewCompletedToday;
    const reviewDone =
      todayProgress?.reviewStatus === "completed" ||
      (reviewsDueToday === 0 && reviewCompletedToday > 0);
    const reviewStatusForCard: WorkStatus = reviewDone
      ? "completed"
      : reviewCompletedToday > 0
      ? "in_progress"
      : "not_started";
    const hasReviewActivity =
      reviewsDueToday > 0 ||
      reviewCompletedToday > 0 ||
      todayProgress?.reviewStatus === "in_progress";
    const reviewDetail = reviewDone
      ? `Today's review complete · ${reviewCompletedToday}/${Math.max(
          reviewTarget,
          reviewCompletedToday,
        )} done`
      : reviewsDueToday > 0
      ? `${reviewsDueToday} review${reviewsDueToday === 1 ? "" : "s"} due today${
          reviewCompletedToday > 0 ? ` · ${reviewCompletedToday} done` : ""
        }`
      : reviewError
      ? "Review queue could not refresh."
      : "Review is clear today. A light read still counts toward the habit.";
    const reviewCta = reviewDone ? "Done" : reviewsDueToday > 0 ? "Review" : "Open";

    const readingGoal = dashboard.readingGoal;
    const readingEnabled = !!readingGoal?.isEnabled;
    const readingDetail = !readingEnabled
      ? "No reading goal set yet. Add one from Targets when reading is part of the day."
      : readingGoal.status === "completed"
      ? `Today's reading complete · ${readingGoal.completedPages}/${readingGoal.targetPages} pages`
      : readingGoal.status === "in_progress"
      ? `${readingGoal.completedPages}/${readingGoal.targetPages} pages read · resume page ${
          readingGoal.lastPage ?? 1
        }`
      : `Read ${readingGoal.targetPages} page${
          readingGoal.targetPages === 1 ? "" : "s"
        } today${readingGoal.lastPage ? ` · saved page ${readingGoal.lastPage}` : ""}`;
    const readingCta =
      readingGoal?.status === "completed"
        ? "Done"
        : readingGoal?.status === "in_progress"
        ? "Resume"
        : "Read";
    const readingStatus = readingGoal?.status ?? "not_started";
    const totalMinutes = dashboard.todaysPlan.totalEstimatedMinutes;
    const story = !child.hideStories ? dashboard.todaysPlan.story : null;
    const dua = !child.hideDuas ? dashboard.todaysPlan.dua : null;
    const hasContentSuggestions = !!story || !!dua;
    const upNextMem = dashboard.upNextMemorization;
    const nextWorkTitle = upNextMem
      ? upNextMem.workLabel ?? "Next Memorization"
      : dashboard.nextSurah
      ? "Next Surah"
      : null;
    const nextWorkDetail = upNextMem
      ? `${upNextMem.surahName} · ${formatAyahRange(upNextMem.ayahStart, upNextMem.ayahEnd)}${
          formatPageRange(upNextMem.pageStart, upNextMem.pageEnd)
            ? ` · ${formatPageRange(upNextMem.pageStart, upNextMem.pageEnd)}`
            : ""
        }`
      : dashboard.nextSurah
      ? `${dashboard.nextSurah.nameTransliteration} · ${dashboard.nextSurah.nameTranslation} · ${dashboard.nextSurah.verseCount} verses`
      : null;
    const allPrimaryWorkComplete =
      (!todaysMem || memStatus === "completed") &&
      (reviewDone || reviewsDueToday === 0) &&
      (!readingEnabled || readingGoal?.status === "completed");
    const noPrimaryWork =
      !todaysMem &&
      reviewsDueToday === 0 &&
      reviewCompletedToday === 0 &&
      !readingEnabled;
    const showCompleteBanner = allPrimaryWorkComplete && !noPrimaryWork;

    return (
      <ScrollView
        style={styles.scroll}
        contentContainerStyle={styles.list}
        refreshControl={
          <RefreshControl
            refreshing={refreshing}
            onRefresh={() => loadDashboard("refresh")}
            tintColor="#2563eb"
          />
        }
      >
        <View style={styles.summaryBand}>
          <Text style={styles.summaryAvatar}>{child.avatarEmoji}</Text>
          <View style={styles.summaryText}>
            <Text style={styles.summaryKicker}>Today</Text>
            <Text style={styles.summaryName}>{child.name}</Text>
            <Text style={styles.summarySubline}>
              {totalMinutes > 0 ? `${totalMinutes} min plan` : "A gentle day is ready"}
            </Text>
          </View>
          <View style={styles.summaryStats}>
            <Text style={styles.summaryStat}>{formatDayStreak(child.streakDays)}</Text>
            <Text style={styles.summaryStatMuted}>{child.totalPoints} pts</Text>
          </View>
        </View>

        {dashboardError && (
          <View style={styles.warningBanner}>
            <Text style={styles.warningText}>
              Today's plan is temporarily unavailable. Pull down to refresh shortly.
            </Text>
          </View>
        )}

        <View style={styles.statsGrid}>
          <StatTile
            label="Surahs"
            value={stats?.totalSurahsMemorized ?? 0}
            iconName="library-outline"
            color="#2563eb"
          />
          <StatTile
            label="Verses"
            value={stats?.totalVersesMemorized ?? 0}
            iconName="text-outline"
            color="#d97706"
          />
          <StatTile
            label="Badges"
            value={earnedAchievements.length}
            iconName="trophy-outline"
            color="#be123c"
          />
        </View>

        {(showCompleteBanner || noPrimaryWork) && (
          <View
            style={[
              styles.dayStateBanner,
              showCompleteBanner ? styles.dayStateComplete : styles.dayStateEmpty,
            ]}
          >
            <Ionicons
              name={showCompleteBanner ? "checkmark-circle-outline" : "leaf-outline"}
              size={19}
              color={showCompleteBanner ? "#047857" : "#0f766e"}
            />
            <Text
              style={[
                styles.dayStateText,
                { color: showCompleteBanner ? "#047857" : "#0f766e" },
              ]}
            >
              {showCompleteBanner
                ? "All core work is complete for today."
                : "No required work is queued. Choose a light review, reading, or target update."}
            </Text>
          </View>
        )}

        <SectionHeader title="Today's Work" />

        <WorkCard
          title="Memorization"
          eyebrow={todaysMem?.isReviewOnly ? "Recitation focus" : "Today's work"}
          detail={memDetail}
          cta={memCta}
          tone={CARD_TONES.memorization}
          onPress={() => handleCardPress("memorization")}
          status={todaysMem ? memStatus : undefined}
        />

        <WorkCard
          title="Review"
          eyebrow="Review queue"
          detail={reviewDetail}
          cta={reviewCta}
          tone={CARD_TONES.review}
          onPress={() => handleCardPress("review")}
          status={hasReviewActivity ? reviewStatusForCard : undefined}
        >
          {dueItems.length > 0 && (
            <View style={styles.reviewPreviewList}>
              {dueItems.slice(0, 3).map((item) => (
                <ReviewPreviewItem key={item.id} item={item} />
              ))}
              {dueItems.length > 3 && (
                <Text style={styles.moreText}>+{dueItems.length - 3} more queued</Text>
              )}
            </View>
          )}
        </WorkCard>

        <WorkCard
          title="Reading"
          eyebrow="Mushaf"
          detail={readingDetail}
          cta={readingCta}
          tone={CARD_TONES.reading}
          onPress={() => handleCardPress("reading")}
          status={readingEnabled ? readingStatus : undefined}
        />

        {nextWorkTitle && nextWorkDetail && (
          <>
            <SectionHeader title="Up Next" />
            <Pressable
              style={styles.nextCard}
              onPress={() => handleCardPress("memorization")}
            >
              <View style={styles.nextNumber}>
                <Text style={styles.nextNumberText}>
                  {upNextMem?.surahNumber ?? dashboard.nextSurah?.number}
                </Text>
              </View>
              <View style={styles.nextText}>
                <Text style={styles.nextTitle}>{nextWorkTitle}</Text>
                <Text style={styles.nextDetail}>{nextWorkDetail}</Text>
              </View>
              {dashboard.nextSurah && !upNextMem ? (
                <Text style={styles.nextArabic}>{dashboard.nextSurah.nameArabic}</Text>
              ) : (
                <Ionicons name="chevron-forward" size={18} color="#2563eb" />
              )}
            </Pressable>
          </>
        )}

        {goals.length > 0 && (
          <>
            <SectionHeader
              title="Goals"
              action="Settings"
              onPress={handleTargetsPress}
            />
            <View style={styles.panel}>
              {goals.slice(0, 2).map((goal) => (
                <GoalPreview key={goal.id} goal={goal} />
              ))}
            </View>
          </>
        )}

        {achievements.length > 0 && (
          <>
            <SectionHeader
              title="Achievements"
              action="More"
              onPress={() => openChildRoute("/child/[childId]/more", child.name)}
            />
            <View style={styles.panel}>
              {achievements.slice(0, 3).map((achievement) => (
                <AchievementPreview key={achievement.id} achievement={achievement} />
              ))}
            </View>
          </>
        )}

        {hasContentSuggestions && (
          <>
            <SectionHeader title="Gentle Extras" />
            <View style={styles.suggestionList}>
              {story && (
                <SuggestionCard
                  title="Today's Story"
                  detail={story.title}
                  iconName="sparkles-outline"
                  color="#2563eb"
                  onPress={() => openChildRoute("/child/[childId]/more", child.name)}
                />
              )}
              {dua && (
                <SuggestionCard
                  title="Today's Du'a"
                  detail={dua.translation}
                  iconName="heart-outline"
                  color="#be123c"
                  onPress={() => openChildRoute("/child/[childId]/more", child.name)}
                >
                  <Text style={styles.duaArabic} numberOfLines={1}>
                    {dua.arabic}
                  </Text>
                </SuggestionCard>
              )}
            </View>
          </>
        )}

        <SectionHeader title="Quick Actions" />
        <View style={styles.quickGrid}>
          <QuickAction
            title="Settings"
            detail="Targets"
            iconName="flag-outline"
            color="#2563eb"
            onPress={handleTargetsPress}
          />
          <QuickAction
            title="Full Quran"
            detail="Read pages"
            iconName="reader-outline"
            color="#0f766e"
            onPress={() => handleCardPress("reading")}
          />
          <QuickAction
            title="Profile"
            detail="Settings"
            iconName="settings-outline"
            color="#4f46e5"
            onPress={() => openChildRoute("/child/[childId]/profile", child.name)}
          />
          <QuickAction
            title="More"
            detail="All tools"
            iconName="grid-outline"
            color="#7c3aed"
            onPress={() => openChildRoute("/child/[childId]/more", child.name)}
          />
        </View>
      </ScrollView>
    );
  }

  return (
    <View style={styles.container}>
      <View style={styles.header}>
        <Pressable onPress={() => router.back()}>
          <Text style={styles.back}>← Back</Text>
        </Pressable>
        <Text style={styles.title}>{name ?? "Dashboard"}</Text>
        <Pressable style={styles.headerRight} onPress={handleTargetsPress}>
          <Text style={styles.targetsText}>Settings</Text>
        </Pressable>
      </View>

      {renderContent()}
      <ChildBottomNav
        active="dashboard"
        childId={navChildId}
        name={navName}
        reviewCount={navReviewCount}
      />
    </View>
  );
}

