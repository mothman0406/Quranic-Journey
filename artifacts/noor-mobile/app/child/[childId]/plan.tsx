export default function PlanScreen() {
  const { childId, name } = useLocalSearchParams<{ childId: string; name: string }>();
  const [state, setState] = useState<PlanState>({ status: "loading" });
  const [refreshing, setRefreshing] = useState(false);
  const [resetting, setResetting] = useState(false);
  const fallbackName = typeof name === "string" ? name : "Plan";

  const loadPlan = useCallback(
    async (mode: "focus" | "refresh" = "focus") => {
      if (!isValidChildId(childId)) {
        setState({
          status: "error",
          message: "This plan route is missing a valid child id.",
        });
        return;
      }

      if (mode === "refresh") {
        setRefreshing(true);
      } else {
        setState((current) => current.status === "ready" ? current : { status: "loading" });
      }

      try {
        const [plan, dashboard] = await Promise.all([
          apiFetch<PlanResponse>(`/api/children/${childId}/plan`),
          apiFetch<DashboardResponse>(`/api/children/${childId}/dashboard?preview=true`),
        ]);
        setState({ status: "ready", plan, dashboard });
      } catch (error) {
        setState({ status: "error", message: describeError(error) });
      } finally {
        setRefreshing(false);
      }
    },
    [childId],
  );

  useFocusEffect(
    useCallback(() => {
      void loadPlan("focus");
    }, [loadPlan]),
  );

  const child = state.status === "ready" ? state.dashboard.child : null;
  const plan = state.status === "ready" ? state.plan : null;
  const practiceMinutes = plan?.practiceMinutesPerDay ?? 20;

  async function resetGoals() {
    if (!isValidChildId(childId)) return;

    setResetting(true);
    try {
      await apiFetch<{ success: boolean; goals: null }>(`/api/children/${childId}/goals`, {
        method: "PUT",
        body: JSON.stringify({ goals: null }),
      });
      await loadPlan("refresh");
    } catch (error) {
      Alert.alert("Could not reset goals", describeError(error));
    } finally {
      setResetting(false);
    }
  }

  function confirmResetGoals() {
    Alert.alert(
      "Reset goals?",
      "NoorPath will replace these goals with the auto-calculated plan for this child.",
      [
        { text: "Cancel", style: "cancel" },
        {
          text: "Reset",
          style: "destructive",
          onPress: () => {
            void resetGoals();
          },
        },
      ],
    );
  }

  return (
    <View style={styles.container}>
      <Header child={child} childId={childId} fallbackName={fallbackName} />

      {state.status === "loading" ? (
        <View style={styles.center}>
          <ActivityIndicator size="large" color="#2563eb" />
        </View>
      ) : state.status === "error" ? (
        <View style={styles.center}>
          <View style={styles.errorCard}>
            <Ionicons name="alert-circle-outline" size={24} color="#dc2626" />
            <Text style={styles.errorText}>{state.message}</Text>
          </View>
          <Pressable style={styles.retryButton} onPress={() => loadPlan("focus")}>
            <Text style={styles.retryText}>Retry</Text>
          </Pressable>
        </View>
      ) : plan ? (
        <ScrollView
          style={styles.scroll}
          contentContainerStyle={styles.list}
          refreshControl={
            <RefreshControl
              refreshing={refreshing}
              onRefresh={() => loadPlan("refresh")}
              tintColor="#2563eb"
            />
          }
        >
          <View style={styles.titleBlock}>
            <Text style={styles.pageTitle} numberOfLines={3}>
              {AGE_GROUP_LABELS[plan.ageGroup] ?? AGE_GROUP_LABELS.child}
            </Text>
            <View style={styles.practicePill}>
              <Ionicons name="time-outline" size={14} color="#2563eb" />
              <Text style={styles.practiceText}>{practiceMinutes} min/day</Text>
            </View>
          </View>

          {plan.goals.length > 0 ? (
            <Card>
              <CardTitle
                iconName="trophy-outline"
                iconColor="#d97706"
                title="Your Goals"
                right={
                  <Pressable
                    accessibilityRole="button"
                    disabled={resetting}
                    hitSlop={8}
                    onPress={confirmResetGoals}
                  >
                    <Text style={[styles.resetText, resetting && styles.resetTextDisabled]}>
                      {resetting ? "Resetting..." : "Reset to auto"}
                    </Text>
                  </Pressable>
                }
              />
              <View style={styles.goalList}>
                {plan.goals.map((goal) => (
                  <GoalCard key={goal.id} goal={goal} />
                ))}
              </View>
              <Text style={styles.goalsFooter}>
                Goals are auto-calculated based on your {practiceMinutes} min/day practice time
              </Text>
            </Card>
          ) : null}

          <Card>
            <Text style={styles.descriptionText}>{plan.description}</Text>
          </Card>

          <Card>
            <CardTitle iconName="locate-outline" iconColor="#2563eb" title="Current Phase" />
            <View style={styles.phasePill}>
              <Text style={styles.phasePillText}>{plan.currentPhase.phaseName}</Text>
            </View>
            <Text style={styles.bodyText}>{plan.currentPhase.description}</Text>
            <View style={styles.chipWrap}>
              {plan.currentPhase.surahs.map((surah) => (
                <View key={surah} style={styles.surahChip}>
                  <Text style={styles.surahChipText}>{surah}</Text>
                </View>
              ))}
            </View>
          </Card>

          <Card>
            <CardTitle iconName="calendar-outline" iconColor="#2563eb" title="Weekly Goals" />
            <View style={styles.weeklyGrid}>
              {WEEKLY_TILES.map((tile) => (
                <WeeklyTile
                  key={tile.key}
                  label={tile.label}
                  value={plan.weeklyGoal[tile.key]}
                  color={tile.color}
                  backgroundColor={tile.backgroundColor}
                  borderColor={tile.borderColor}
                />
              ))}
            </View>
          </Card>

          {plan.milestones.length > 0 ? (
            <Card>
              <CardTitle iconName="star-outline" iconColor="#d97706" title="Milestones" />
              <View style={styles.rowList}>
                {plan.milestones.map((milestone, index) => (
                  <MilestoneRow
                    key={milestone.id}
                    milestone={milestone}
                    isLast={index === plan.milestones.length - 1}
                  />
                ))}
              </View>
            </Card>
          ) : null}

          {plan.tajweedRules.length > 0 ? (
            <Card>
              <CardTitle iconName="book-outline" iconColor="#0f766e" title="Tajweed Rules" />
              <View style={styles.rowList}>
                {plan.tajweedRules.map((rule, index) => (
                  <TajweedRuleRow
                    key={rule.rule}
                    rule={rule}
                    isLast={index === plan.tajweedRules.length - 1}
                  />
                ))}
              </View>
            </Card>
          ) : null}
        </ScrollView>
      ) : null}

      <ChildBottomNav
        active="more"
        childId={childId}
        name={child?.name ?? fallbackName}
        reviewCount={state.status === "ready" ? state.dashboard.reviewsDueToday : undefined}
      />
    </View>
  );
}

