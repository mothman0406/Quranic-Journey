function GoalCard({ goal }: { goal: Goal }) {
  const current = goal.currentCount ?? 0;
  const target = goal.targetSurahCount ?? 0;
  const completed = target > 0 && current >= target;
  const percent = completed ? 100 : formatPercent(current, target);
  const daysLeft = getDaysLeft(goal.targetDate);
  const targetLabel = formatTargetDate(goal.targetDate);

  return (
    <View style={[styles.goalCard, completed && styles.goalCardComplete]}>
      <View style={styles.goalHeader}>
        <View style={styles.goalTitleBlock}>
          <Text style={styles.goalTitle} numberOfLines={2}>
            {goal.title}
          </Text>
          <Text style={styles.goalDescription}>{goal.description}</Text>
        </View>
        {completed ? (
          <View style={styles.completedBadge}>
            <Text style={styles.completedBadgeText}>🏆 Completed!</Text>
          </View>
        ) : (
          <View style={styles.daysBadge}>
            <Text style={styles.daysBadgeText}>{formatDaysLeft(daysLeft)}</Text>
          </View>
        )}
      </View>

      <ProgressBar value={percent} color={completed ? "#059669" : "#2563eb"} />

      <View style={styles.goalProgressRow}>
        <Text style={styles.goalCountText}>
          {current} / {target} surahs
        </Text>
        <Text style={styles.goalPercentText}>{percent}%</Text>
      </View>

      {!completed && targetLabel ? (
        <Text style={styles.targetDateText}>Target: {targetLabel}</Text>
      ) : null}
    </View>
  );
}

