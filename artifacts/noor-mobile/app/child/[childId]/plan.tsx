function isValidChildId(childId: string | undefined): childId is string {
  return typeof childId === "string" && /^\d+$/.test(childId);
}

function describeError(error: unknown) {
  if (error instanceof ApiError) {
    return `${error.message} (${error.path})`;
  }
  if (error instanceof Error) {
    return error.message;
  }
  return "Failed to load the learning plan.";
}

function parseLocalDate(value: string | undefined) {
  if (!value) return null;
  const date = new Date(`${value}T12:00:00`);
  return Number.isNaN(date.getTime()) ? null : date;
}

function getLocalDateValue() {
  const date = new Date();
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function getDaysLeft(targetDate: string | undefined) {
  const target = parseLocalDate(targetDate);
  const today = parseLocalDate(getLocalDateValue());
  if (!target || !today) return null;
  return Math.max(0, Math.ceil((target.getTime() - today.getTime()) / 86_400_000));
}

function formatDaysLeft(days: number | null) {
  if (days == null) return "Target pending";
  if (days === 0) return "Due today";
  return `${days} day${days === 1 ? "" : "s"} left`;
}

function formatTargetDate(value: string | undefined) {
  const date = parseLocalDate(value);
  if (!date) return null;
  return date.toLocaleDateString("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
  });
}

function formatPercent(value: number, target: number) {
  if (target <= 0) return 0;
  return Math.max(0, Math.min(100, Math.round((value / target) * 100)));
}

function Header({
  child,
  childId,
  fallbackName,
}: {
  child: PlanChild | null;
  childId: string | undefined;
  fallbackName: string;
}) {
  const router = useRouter();
  const displayName = child?.name ?? fallbackName ?? "Plan";

  function goDashboard() {
    if (!isValidChildId(childId)) {
      router.back();
      return;
    }

    router.replace({
      pathname: "/child/[childId]",
      params: { childId, name: displayName },
    });
  }

  function openSettings() {
    if (!isValidChildId(childId)) return;
    router.push({
      pathname: "/child/[childId]/targets",
      params: { childId, name: displayName },
    });
  }

  return (
    <View style={styles.header}>
      <Pressable
        accessibilityRole="button"
        accessibilityLabel="Back to dashboard"
        hitSlop={8}
        style={styles.headerIconButton}
        onPress={goDashboard}
      >
        <Ionicons name="chevron-back" size={22} color="#111827" />
      </Pressable>

      <View style={styles.headerIdentity}>
        <Text style={styles.headerAvatar}>{child?.avatarEmoji ?? "?"}</Text>
        <Text style={styles.headerChildName} numberOfLines={1}>
          {displayName}
        </Text>
      </View>

      <Pressable
        accessibilityRole="button"
        accessibilityLabel="Open child settings"
        hitSlop={8}
        style={styles.headerIconButton}
        onPress={openSettings}
      >
        <Ionicons name="settings-outline" size={21} color="#111827" />
      </Pressable>
    </View>
  );
}

